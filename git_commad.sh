# 二、初始化git仓库
#=============================================================================
#=============================================================================

#-----------------------------------------------------------------------------
# 方式1、新建本地仓库，并与远程版本库进行连接
#-----------------------------------------------------------------------------

git init
git remote add origin git@github.com:zhaojinjian0000/git_learn.git
# 查看他 git remote -v
# 删除他 git remote rm origin
git pull origin master  #获取名为origin的远程版本库的提交信息到本地版本库


#-----------------------------------------------------------------------------
# 方式2、获取远程的版本库到本地
#-----------------------------------------------------------------------------

git clone git@github.com:github/hub.git  #该命令相当于上面三步的缩写









#=============================================================================
#=============================================================================
# 三、修改文件并提交到暂存区
#=============================================================================
#=============================================================================

# 将一个修改后的文件添加到暂存区
git add readme.md
# gitadd其他用法
# 添加所有修改、删除或新建的文件到暂存区
git add .
# 添加所有以js结尾的文件到暂存区
git add *.js
# 添加所有修改、删除或新建的文件到暂存区
# 除了.开头的文件，比如 .gitignore
git add *
# git add --update 的缩写
# 如果再次修改了在暂存区中的文件，可以通过该命令进行更新
git add -u
# 作用与git add . 相同
git add -A


#=============================================================================
#=============================================================================
# 四、提交代码到版本库
#=============================================================================
#=============================================================================

# 提交暂存区的代码到版本库
git commit -m 'commit message'

# 如果你重新编辑了一些文件，添加到暂存区，想把这些修改合并到上一次提交
# 然后会出现一个编辑框，让你修改上次的提交信息
git commit --amend
# 如果不想修改上次的提交信息
git commit --amend --no-edit


#=============================================================================
#=============================================================================
# 五、同步远程版本库到本地版本库
#=============================================================================
#=============================================================================

# 将本地的提交同步给远程版本库
git push origin master

# 绑定默认提交的远程版本库
git push -u origin master
# 下次提交只需要使用git push就可以了
git push




#=============================================================================
#=============================================================================
# 六、git分支
#=============================================================================
#=============================================================================

# 查看当前分支
git branch #刚刚初始化的版本库默认在master分支上
# 新建分支
git branch branch #新建一个名为branch的分支

# 切换分支
git checkout branch #切换到branch分支

# 创建分支与切换分支可以简写为一个命令
gti checkout -b branch

